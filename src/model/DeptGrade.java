package model;

public class DeptGrade {
	private String dname;
	private double gscore;
	public String getDname() {
		return dname;
	}
	public void setDname(String dname) {
		this.dname = dname;
	}
	public double getGscore() {
		return gscore;
	}
	public void setGscore(double gscore) {
		this.gscore = gscore;
	}
}
