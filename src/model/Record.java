package model;

public class Record {
	private int year;
	private String sno;
	private double height;
	private double weight;
	private int fhl;
	private double run50;
	private int jump;
	private double zwt;
	private double run800;
	private double run1000;
	private int ywqz;
	private int ytxs;
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public String getSno() {
		return sno;
	}
	public void setSno(String sno) {
		this.sno = sno;
	}
	public double getHeight() {
		return height;
	}
	public void setHeight(double height) {
		this.height = height;
	}
	public double getWeight() {
		return weight;
	}
	public void setWeight(double weight) {
		this.weight = weight;
	}
	public int getFhl() {
		return fhl;
	}
	public void setFhl(int fhl) {
		this.fhl = fhl;
	}
	public double getRun50() {
		return run50;
	}
	public void setRun50(double run50) {
		this.run50 = run50;
	}
	public int getJump() {
		return jump;
	}
	public void setJump(int jump) {
		this.jump = jump;
	}
	public double getZwt() {
		return zwt;
	}
	public void setZwt(double zwt) {
		this.zwt = zwt;
	}
	public double getRun800() {
		return run800;
	}
	public void setRun800(double run800) {
		this.run800 = run800;
	}
	public double getRun1000() {
		return run1000;
	}
	public void setRun1000(double run1000) {
		this.run1000 = run1000;
	}
	public int getYwqz() {
		return ywqz;
	}
	public void setYwqz(int ywqz) {
		this.ywqz = ywqz;
	}
	public int getYtxs() {
		return ytxs;
	}
	public void setYtxs(int ytxs) {
		this.ytxs = ytxs;
	}
	
	
}
